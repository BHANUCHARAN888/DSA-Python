# some string methods
a=["red", "green", "blue"]
print(a)
# append 
a.append("yellow")
print(a)
# insert
a.insert(2,"pink")
print(a)
# remove
a.remove("green")
print(a)
# pop
a.pop(1)
print(a)
# sort
a.sort()
print(a)
# reverse
a.reverse()
print(a)
# clear
a.clear()
print(a)
#index
a=["red", "green", "blue"]
print(a.index("green"))
# count
print(a.count("red"))
# join
b = " ".join(a)
print(b)